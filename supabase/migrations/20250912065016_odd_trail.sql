**2. Vestibüler Sistem (Denge)**
- İç kulakta bulunan ve hareket, denge, uzaysal yönelim bilgilerini işler
- Dikkat, uyarılma düzeyi ve postural kontrolü etkiler
- Öğrenme ve akademik başarı için temel oluşturur

**3. Proprioseptif Sistem (Kas-Eklem)**
- Kaslar, eklemler ve bağlardan gelen konum bilgisi
- Vücut farkındalığı ve motor planlama için gerekli
- Öz düzenleme ve davranış kontrolünde rol oynar

