## Duyusal Sistemlerimizin Rolü

### Temel Duyusal Sistemler

Geleneksel olarak bildiğimiz beş duyu organının yanı sıra, duyu bütünleme teorisi üç temel duyusal sisteme odaklanır:

**1. Dokunsal Sistem (Taktil)**
- Cilt yoluyla aldığımız dokunma, basınç, sıcaklık ve ağrı bilgileri
- Sosyal etkileşim ve duygusal düzenleme için kritik
